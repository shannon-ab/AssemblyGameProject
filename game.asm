	lw $t0, double_state
	bne $t0, $zero, end_while 	# if it already double jump
	
	la $t0, double_state		# change double_state to 1
	li $t1, 1
	sw $t1, 0($t0)
    	
	la $t0, jump_state
	li $t1, 3 			# set jump state to 4
	sw $t1, 0($t0)
	jal buzz_jump

